public class dataVariables_18Jan {
    public static void main(String[] args) {
        System.out.println("Boolean Data type");
        boolean b = true;
        if (b ) {
            System.out.println("Boolean value is true");
        } else {
            System.out.println("Boolean value is false");
        }
        System.out.println("Character Data type");
        char c = 'A';
        char c1 = 65;
        System.out.println("Character value is " + c);
        System.out.println("Character value is " + c1);
        System.out.println("Integer Data type");
        int i = 10;
        long l = 100000000000000000L;
        short s = 100;
        byte b1 = 127;
        System.out.println("Integer value is " + i);
        System.out.println("Long value is " + l);
        System.out.println("Short value is " + s);
        System.out.println("Byte value is " + b1);
        System.out.println("Floating Data type");
        float f = 10.52323f;
        double d = 10.543543;
        System.out.println("Float value is " + f);
        System.out.println("Double value is " + d);

    }
}
